/**
 Tipos de Dados Primitivos:

Número (Number): Números simples.
Exemplo: const idade = 25;

String: Textos entre aspas.
Exemplo: const nome = "Alice";

Booleano (Boolean): Verdadeiro (true) ou Falso (false).
Exemplo: const estaChovendo = true;

Null: Valor vazio intencional.
Exemplo: const valorNulo = null;

Undefined: Valor quando uma variável é declarada, mas não possui valor.
Exemplo: let valorIndefinido;
 */

/*
Tipos de Dados por Referência:

Objeto (Object): Coleção de informações.
Array: Lista ordenada de valores.
Função (Function): Bloco de código reutilizável.
*/
/**
 * Quando você altera um valor de um tipo de dado primitivo, não afeta outros valores. Quando você altera um objeto ou array (tipos por referência), eles podem ser compartilhados por várias variáveis, e mudar um valor pode afetar todos os lugares onde o objeto ou array é usado.
 */